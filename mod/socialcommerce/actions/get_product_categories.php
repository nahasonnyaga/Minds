		$product_type_id = get_input('product_type',1);	
		$category_guid = get_input('category_guid',0);
		$current_parent = get_input('parent_guid',0);
		$current_page = get_input('page','');		
		if($current_page == 'category'){
			$checked = "";
			if($current_parent==0){
				$checked = "checked";
			}
			$disply_cat ='<div class="nocategory"><input type = "radio" name="category_selected" '.$checked.' value="0">'.elgg_echo('category:noparent').'</input></div>';
		}		
		if($product_type_id > 0){
			$disply_cat .= get_categories(0,$category_guid,$product_type_id,$current_parent);
		}	
		echo $disply_cat;
